# ── 유저 프롬프트 빌더 ────────────────────────────────────────────────────────

def build_user_prompt(
    draft: str,
    question: str,
    company: str,
    references: list[dict],
    avg_ref_chars: int = 0,
    jd_summary: str = "",
    question_type: str = "",
    max_chars: int = 0,
) -> str:
    """
    사용자 프롬프트 조합.

    Parameters
    ----------
    draft         : 사용자 자소서 초안
    question      : 자소서 문항
    company       : 지원 기업명
    references    : retrieve() 반환값 리스트
    avg_ref_chars : 참고 자소서 평균 글자수 (0이면 미사용)
    jd_summary    : 직무 기술서 요약
    question_type : 질문 유형 ('growth'/'experience'/'motivation' 등, 미분류면 '')
    max_chars     : 문항별 글자수 상한 직접 지정 (0이면 avg_ref_chars 기준 사용)

    Returns
    -------
    str: Claude 에게 전달할 user turn 텍스트
    """
    # 참고 자소서 섹션
    if references:
        lines = ["## 참고 합격자소서 (유사도 높은 순)\n"]
        for i, r in enumerate(references, 1):
            ans = r.get("answer") or ""
            ans_preview = ans[:REF_MAX_CHARS]
            if len(ans) > REF_MAX_CHARS:
                ans_preview += "…"

            q_preview = (r.get("question") or "")[:100]
            lines.append(
                f"[참고{i}] {r['company']} / {r.get('role') or '직무미상'} "
                f"(유사도 {r['similarity']:.2f})\n"
                f"Q: {q_preview}\n"
                f"A: {ans_preview}\n"
            )
        ref_section = "\n".join(lines)
    else:
        ref_section = "## 참고 자소서\n(해당 기업 및 유사 기업의 합격 데이터를 찾지 못했습니다. 초안만으로 첨삭합니다.)"

    # 글자수 지시 (max_chars 직접 지정 > avg_ref_chars 기준)
    if max_chars > 0:
        char_guide = (
            f"\n## 목표 글자수\n"
            f"rewrite는 반드시 {max_chars}자 이내로 작성하세요. (공백 포함)\n"
            f"단, 원문 내용이 부족하면 없는 사실을 추가하는 것보다 짧게 작성하는 것이 낫습니다."
        )
    elif avg_ref_chars > 0:
        char_min = int(avg_ref_chars * 0.9)
        char_max = int(avg_ref_chars * 1.1)
        char_guide = (
            f"\n## 목표 글자수\n"
            f"참고 합격자소서 평균 글자수: {avg_ref_chars}자\n"
            f"rewrite는 가능한 한 {char_min}~{char_max}자 사이를 목표로 작성하세요. (공백 포함)\n"
            f"단, 원문 내용이 부족하면 없는 사실을 추가하는 것보다 짧게 작성하는 것이 낫습니다."
        )
    else:
        char_guide = ""

    # 유형별 강조 안내
    type_guide = ""
    if question_type and question_type in _TYPE_EMPHASIS:
        type_guide = f"\n## 첨삭 강조점 ({question_type} 유형)\n{_TYPE_EMPHASIS[question_type]}\n"

    prompt = (
        f"## 지원 기업\n{company}\n\n"
        f"## 자소서 문항\n{question}\n\n"
        + (f"{jd_summary}\n\n" if jd_summary else "")
        + f"## 사용자 초안 ({len(draft)}자)\n{draft}\n\n"
        f"{ref_section}"
        f"{char_guide}"
        f"{type_guide}\n\n"
        "위 초안을 첨삭해 주세요. "
        + ("인재상·핵심역량을 반영하여 " if jd_summary else "")
        + "JSON 형식으로만 응답하세요."
    )
    return prompt
